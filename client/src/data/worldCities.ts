/** Get popular destinations (top-rated tourist hotspots) */
export function getPopularDestinations(limit = 8): WorldCity[] {
  return WORLD_CITIES.filter((c) => c.isPopular).slice(0, limit);
}

/** Get all unique category values from the dataset */
export function getAllCategories(): string[] {
  const cats = new Set<string>();
  WORLD_CITIES.forEach((c) => c.categories.forEach((cat) => cats.add(cat)));
  return Array.from(cats).sort();
}
